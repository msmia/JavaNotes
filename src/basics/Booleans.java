package basics;

public class Booleans {
	
	
	public static void main(String[] args) {
		
		int val1 = 10;
		int val2 = 11;

		System.out.println("is 10 equal to 11? " + (val1 == val2));
		System.out.println("is 10 not equal to 11? " + (val1 != val2));
		System.out.println("is 10 less than 11? " + (val1 < val2));
		System.out.println("is 10 greater than 11? " + (val1 > val2));
		System.out.println("is 10 less than or equal to 11? " + (val1 <= val2));
		System.out.println("is 10 greater than or equal to 11? " + (val1 >= val2));
	}

}
